function loadTest() {
  const version = document.getElementById("version").value;
  const testData = testVersions[version];
  const form = document.getElementById("testForm");
  form.innerHTML = "";

  testData.forEach((q, idx) => {
    const questionDiv = document.createElement("div");
    questionDiv.classList.add("question");

    const questionHTML = `<strong>${q.question}</strong>`;
    const optionsHTML = q.options.map((opt, i) => `
      <label>
        <input type="radio" name="q${idx}" value="${i}"> ${opt}
      </label>
    `).join("");

    questionDiv.innerHTML = `${questionHTML}<div class="answers">${optionsHTML}</div>`;
    form.appendChild(questionDiv);
  });

  document.getElementById("result").innerText = "";
}

function submitTest() {
  const version = document.getElementById("version").value;
  const testData = testVersions[version];
  const form = document.getElementById("testForm");
  let correct = 0;

  testData.forEach((q, idx) => {
    const selected = form.querySelector(`input[name="q${idx}"]:checked`);
    if (selected && parseInt(selected.value) === q.answer) {
      correct++;
    }
  });

  document.getElementById("result").innerText = `Дұрыс жауаптар саны: ${correct} / ${testData.length}`;
}
